module encoder_4to2
	(
		d_out, 
		d_in
	);

/////////////////////////////////////////////////////////////////////////
// Parameter Declarations

/////////////////////////////////////////////////////////////////////////
// Port Declarations
input [3:0]   d_in;
/////////////////////////////////////////////////////////////////////////
// Output Declarations
output [1:0] d_out;

/////////////////////////////////////////////////////////////////////////
// Local Logic and Instantiation
assign d_out[1] = d_in[3] | d_in[2];
assign d_out[0] = d_in[3] | ((~d_in[2])&d_in[1]);
endmodule